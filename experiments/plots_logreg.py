def plot_litlvm_pSigma_gamma(data, p, sigma, output_file="plot.pdf"):
    step = 3
    # Group the data
    grouped_data = [data[i:i+step] for i in range(0, len(data), step)]

    # Calculate means and standard deviations
    means = []
    std_devs = []
    for group in grouped_data:
        group_means = [np.mean(region) for region in group]
        group_std_devs = [np.std(region) for region in group]
        means.append(group_means)
        std_devs.append(group_std_devs)

    # Create the plot
    fig, ax = plt.subplots(figsize=(12, 5))
    x = np.arange(len(grouped_data))  # the label locations
    width = 0.2  # the width of the bars

    # Labels for the legends
    legend_labels = ['d:2', 'd:5', 'd:10']
    colors = sns.color_palette("viridis", n_colors=step)
    for i in range(step):  # Plot bars for each dimension
        means_plot = [group[i] for group in means]
        std_devs_plot = [group[i] for group in std_devs]
        ax.bar(x - width + i * width, means_plot, width, label=legend_labels[i], 
            yerr=std_devs_plot, capsize=5, color=colors[i])

    # Customize labels, legend, and axis
    ax.set_ylabel('Mean AUC', fontweight='bold')
    ax.set_xticks(x)
    ax.set_xticklabels(['$\gamma=0.01$', '$\gamma=0.1$', '$\gamma=1$', '$\gamma=10$'], fontsize=12, fontweight='bold')
    ax.legend()
    ax.set_ylim(0.5, 0.7)

    # Add text annotation for p and sigma
    annotation_text = f"$p={p}, \sigma={sigma}$"
    ax.text(1.5, 0.68, annotation_text, fontsize=12, fontweight='bold', ha='center')

    # Adjust layout and save the plot
    fig.tight_layout()
    plt.savefig(output_file, dpi=1000, bbox_inches='tight')
    plt.show()



